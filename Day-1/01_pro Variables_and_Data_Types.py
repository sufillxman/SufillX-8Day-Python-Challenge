# Python Beginner Notes: Variables and Data Types

# 1. Variables in Python
# Variables are used to store data. You can give any name to a variable.
# Example:
name = "Sufill"
age = 25

# 2. Data Types in Python
# Python has different data types. Some common ones are:

# a. String (str): Text data
city = "Delhi"

# b. Integer (int): Whole numbers
year = 2024

# c. Float: Decimal numbers
price = 99.99

# d. Boolean (bool): True or False values
is_student = True

# 3. How to check data type?
# Use the type() function.
print(type(name))      # Output: <class 'str'>
print(type(age))       # Output: <class 'int'>
print(type(price))     # Output: <class 'float'>
print(type(is_student))# Output: <class 'bool'>

# 4. Assigning values to variables
# You can assign values directly.
x = 10
y = 20.5
z = "Python"

# 5. Multiple assignment
a, b, c = 1, 2.5, "Hello"
print(a, b, c)         # Output: 1 2.5 Hello

# 6. Example: Using variables
student_name = "sufill"
student_age = 18
print("Name:", student_name)
print("Age:", student_age)

# 7. Rules for variable names:
# - Only letters, numbers, and underscores (_)
# - Cannot start with a number
# - Case sensitive (age and Age are different)

# Example of valid and invalid variable names:
my_var = 10      # valid
_myvar = 20      # valid
# 2myvar = 30    # invalid (cannot start with number)
MyVar = 40       # valid (case sensitive)





